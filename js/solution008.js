// Keep up the hoop

function hoopCount(n) {
  // p: integer, number of times the hoop goes round
  // r: string, an encouraging message
  // e: hoopCount(10) returns "Great, now move on to tricks"
  // p: if, else statement returning a string based on number of hoops

  if (n >= 10) {
    return "Great, now move on to tricks";
  } else {
    return "Keep at it until you get it";
  }
}
